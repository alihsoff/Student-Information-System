    private void btn_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removeActionPerformed
        int std_id = Integer.parseInt(jTextField_STD_ID.getText());
        int crs_id = crs.getCourseId(jComboBoxCourseName.getSelectedItem().toString());
            
        scr.insertUpdateDeleteScore('d', std_id, crs_id,0,null);

        jTable1.setModel(new DefaultTableModel(null, new Object[]{"Ad","Soyad","Dərs adı","Qiymət","Qeyd"}));
        scr.fillScoreJtable(jTable1);
        model = (DefaultTableModel) jTable1.getModel();
        
        jTextField_STD_ID.setText("");
        jComboBoxCourseName.setSelectedIndex(1);
        jTextField_score.setText("");
        jTextField_Description.setText("");
    }//GEN-LAST:event_btn_removeActionPerformed

