    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        float score = Float.parseFloat(jTextField_score.getText());
        String desc = jTextField_Description.getText();
        
        int std_id = Integer.parseInt(jTextField_STD_ID.getText());
        int crs_id = crs.getCourseId(jComboBoxCourseName.getSelectedItem().toString());
            scr.insertUpdateDeleteScore('u', std_id, crs_id, score, desc);

        jTable1.setModel(new DefaultTableModel(null, new Object[]{"Ad","Soyad","Dərs adı","Qiymət","Qeyd"}));
        scr.fillScoreJtable(jTable1);
        
        model = (DefaultTableModel) jTable1.getModel();
    }//GEN-LAST:event_btn_editActionPerformed

    public void selectItem(int rowIndex){
       jTextField_STD_ID.setText(String.valueOf(std.getStudentId(model.getValueAt(rowIndex, 0).toString())));
       jComboBoxCourseName.setSelectedItem(model.getValueAt(rowIndex, 2).toString());
       jTextField_score.setText(model.getValueAt(rowIndex, 3).toString());
       jTextField_Description.setText(model.getValueAt(rowIndex, 4).toString());
    }
